class Standing {
  final String name;
  final String logo;
  final int position;
  final int wins;
  final int losses;
  final int ties;
  final int pointsFor;
  final int pointsAgainst;
  final int netPoints;
  final String streak;

  Standing({
    required this.name,
    required this.logo,
    required this.position,
    required this.wins,
    required this.losses,
    required this.ties,
    required this.pointsFor,
    required this.pointsAgainst,
    required this.netPoints,
    required this.streak,
  });
}