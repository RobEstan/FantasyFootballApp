class Team {
  final String abbreviation;
  final String name;
  final String city;
  final String coach;
  final String stadium;
  final String logo;
  final int id;

  Team({
    required this.abbreviation,
    required this.name,
    required this.city,
    required this.coach,
    required this.stadium,
    required this.logo,
    required this.id,
  });
}