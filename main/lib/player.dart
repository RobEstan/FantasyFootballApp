class Player {
  final int id;
  final String name;
  final int age;
  final String height;
  final String weight;
  final String position;
  final String number;
  final String image;

  Player({
    required this.id,
    required this.name,
    required this.age,
    required this.height,
    required this.weight,
    required this.position,
    required this.number,
    required this.image,
  });
}